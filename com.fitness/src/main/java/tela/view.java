    private void carregaTabela() {

        DefaultTableModel ct = (DefaultTableModel) jTable1.getModel();

        ct.setNumRows(0);
        try {
            String url = "jdbc:mysql://localhost:3306/fitness,?user=root&password=";
            Connection conn = DriverManager.getConnection(url);
            PreparedStatement ps;
            ResultSet rs;

            ps = conn.prepareStatement("select * from alunos;");
            rs = ps.executeQuery();

            while (rs.next()) {
                ct.addRow(new Object[]{
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getInt(3)

                });

            }//while
            conn.close();
            ps.close();
            rs.close();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Ol√° deu erro para Carregar o Banco de Dados \n"
                    + "" + e, "Santinho Avisa", JOptionPane.INFORMATION_MESSAGE);
        }//catch

    }

