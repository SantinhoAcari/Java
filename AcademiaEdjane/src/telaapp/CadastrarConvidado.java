    private void btCadastrarConvidadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarConvidadoActionPerformed

        Convidados bdconvidados = new Convidados();
        bdconvidados.setNome_convidado(txtnomeConvidado.getText());
        bdconvidados.setCpf_convidado(txtcpfConvidado.getText());
        bdconvidados.setValorApagar(txtmensalidadeConvidado.getText());

        bdconvidados.cadastrarCovidado();

        txtcpfConvidado.setText("");
        txtmensalidadeConvidado.setText(" ");
        txtnomeConvidado.setText("");
    }//GEN-LAST:event_btCadastrarConvidadoActionPerformed

