    private void txtmensalidadeConvidadoCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtmensalidadeConvidadoCaretUpdate
        int mensalidade = Integer.parseInt(txtmensalidadeConvidado.getText());
        if (mensalidade < 0) {
            
            JOptionPane.showMessageDialog(null, "Um valor Negativo foi digitado \n "
                    + "Por Favor Redigite o Valor Pago");
        }
    }//GEN-LAST:event_txtmensalidadeConvidadoCaretUpdate

