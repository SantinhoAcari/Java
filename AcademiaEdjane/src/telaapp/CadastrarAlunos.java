    public void validaAluno() {

        int idade = Integer.parseInt(txt_idade.getText());
        if (idade < 10) {
            JOptionPane.showMessageDialog(null, "Aluno Muito Novo para \n"
                    + " ser bodybuilder desta academia volte após completar 10 anos");

        } else {
            String idade2 = Integer.toString(idade);
            txt_idade.setText(idade2);

        }
    }

