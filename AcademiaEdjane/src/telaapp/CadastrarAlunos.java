    private void txt_idadeCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_idadeCaretUpdate
        int idade = Integer.parseInt(txt_idade.getText());
        if (idade == 9 || idade == 8 || idade == 7 || idade == 6 || idade == 5 || idade < 0) {
            JOptionPane.showMessageDialog(null, "Muito Novo para "
                    + "ser aluno desta academia \n volte após completar 10 anos\n"
                    + "Ou você Digitou uma Data Inválida");

        } else {
            String idade2 = Integer.toString(idade);
            txt_idade.setText(idade2);
        }
    }//GEN-LAST:event_txt_idadeCaretUpdate

